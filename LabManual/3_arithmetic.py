#Q3. Write a python Program for addition, Subtraction, multiplication, division 

num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))

add = num1 + num2
subtract = num1 - num2
multiply = num1 * num2
divide = num1 / num2

print(f"Addition of {num1} and {num2}:", add)
print(f"Subtraction of {num1} and {num2}:", subtract)
print(f"Multiplication of {num1} and {num2}:", multiply)
print(f"Division of {num1} and {num2}:", divide)
